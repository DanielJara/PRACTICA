
public class Reloj {

	int hora;
	int minutos;
	int segundos;
	
	public Reloj() {
		
		hora = 12;
		minutos = 36;
		segundos = 52;
	}

	
	
	
	public int getHora() {
		return hora;
	}

	public void setHora(int hora) {
		this.hora = hora;
	}

	public int getMinutos() {
		return minutos;
	}

	public void setMinutos(int minutos) {
		this.minutos = minutos;
	}

	public int getSegundos() {
		return segundos;
	}

	public void setSegundos(int segundos) {
		this.segundos = segundos;
	}
	
	
}
